/**
 * Writes src/generated/deployVersion.ts for the secret "Who I am" tooltip.
 *
 * - Any Vercel build (VERCEL=1) + KV env at build time:
 *   INCR dezc:site-deploy-version; label = v0.01 × counter.
 * - Local / no VERCEL: if KV env exists, GET only (no bump). Else deploy-version.json.
 *
 * KV vars must be enabled for "Build" in Vercel, or prebuild never sees them → v0.00 forever.
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const outDir = join(root, 'src', 'generated')
const outFile = join(outDir, 'deployVersion.ts')
const fallbackPath = join(root, 'deploy-version.json')
const REDIS_KEY = 'dezc:site-deploy-version'

function readFallbackCounter() {
  if (!existsSync(fallbackPath)) return 0
  try {
    const j = JSON.parse(readFileSync(fallbackPath, 'utf8'))
    return Math.max(0, Math.floor(Number(j.counter) || 0))
  } catch {
    return 0
  }
}

function labelFromCounter(n) {
  const num = Math.max(0, Math.floor(Number(n) || 0))
  const v = (num * 0.01).toFixed(2)
  return `v${v}`
}

async function main() {
  let counter = readFallbackCounter()

  const url = (process.env.KV_REST_API_URL ?? process.env.UPSTASH_REDIS_REST_URL ?? '').trim()
  const token = (process.env.KV_REST_API_TOKEN ?? process.env.UPSTASH_REDIS_REST_TOKEN ?? '').trim()

  if (url && token) {
    try {
      const { Redis } = await import('@upstash/redis')
      const redis = new Redis({ url, token })
      const onVercel = process.env.VERCEL === '1'

      if (onVercel) {
        const n = await redis.incr(REDIS_KEY)
        counter = Number(n)
        console.info('[deploy-version]', { incr: true, vercelEnv: process.env.VERCEL_ENV ?? '' })
      } else {
        const raw = await redis.get(REDIS_KEY)
        if (raw != null) {
          counter = Number(raw)
        }
        console.info('[deploy-version]', { incr: false, readRedis: raw != null })
      }
    } catch {
      console.info('[deploy-version]', { redisFailed: true })
      /* keep fallback */
    }
  } else {
    console.info('[deploy-version]', { hasKv: false })
  }

  if (!Number.isFinite(counter) || counter < 0) {
    counter = readFallbackCounter()
  }

  const label = labelFromCounter(counter)
  mkdirSync(outDir, { recursive: true })
  const content =
    `// Generated by scripts/write-deploy-version.mjs — do not edit\n` +
    `export const DEPLOY_VERSION_LABEL = ${JSON.stringify(label)} as const\n`
  writeFileSync(outFile, content, 'utf8')
  console.info('[deploy-version] wrote', label)
}

await main()
